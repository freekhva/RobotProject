package mystic_T;

public class Kaart {
	//variabelen die we nodig hebben
	int rood;
	int blauw;
	int groen;
	
	//constructor
	public Kaart(int rood, int blauw, int groen) {
		super();
		this.rood = rood;
		this.blauw = blauw;
		this.groen = groen;
	}

	public int getRood() {
		return rood;
	}

	public int getBlauw() {
		return blauw;
	}

	public int getGroen() {
		return groen;
	}
	
	
}
